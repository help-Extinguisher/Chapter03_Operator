package ch03_1_operator_expression;

public class ArithmeticOperatorExample {
	public static void main(String[] args) {
		
		int v1 = 5;
		int v2 = 2;
		
		int result1 = v1 + v2;
		System.out.printf("result1= " + result1 + "\n");
		
		int result2 = v1 - v2;
		System.out.printf("result2= " + result2 + "\n");
		
		int result3 = v1 * v2;
		System.out.printf("result3= " + result3 + "\n");
		
		int result4 = v1 / v2;
		System.out.printf("result4= " + result4 + "\n");
		
		int result5 = v1 % v2;
		System.out.printf("result5= " + result5 + "\n");
		
		double result6 = (double) v1 / v2;
		System.out.printf("result6= " + result6 + "\n");
		
		

		
	}

}
